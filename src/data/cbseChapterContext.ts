// ── Lookup helper ──────────────────────────────────────────────────────────

const ALL_CONFIGS: SubjectConfig[] = [PHYSICS_CLASS_11, CHEMISTRY_CLASS_11, BIOLOGY_CLASS_11, MATHS_CLASS_11];

/**
 * Get chapter context for a given chapter title, subject, and class.
 * Uses fuzzy matching on chapter title.
 */
export function getChapterContext(
  chapterTitle: string,
  subject?: string,
  classLevel?: string,
): ChapterContext | null {
  const titleLC = chapterTitle.toLowerCase();
  for (const config of ALL_CONFIGS) {
    if (subject && config.subject.toLowerCase() !== subject.toLowerCase()) continue;
    if (classLevel && !config.classLevel.toLowerCase().includes(classLevel.toLowerCase().replace('class ', ''))) continue;
    const found = config.chapters.find(c =>
      c.chapter.toLowerCase() === titleLC ||
      titleLC.includes(c.chapter.toLowerCase()) ||
      c.chapter.toLowerCase().includes(titleLC)
    );
    if (found) return found;
  }
  return null;
}
