/**
 * CBSE Chapter Context — syllabus weightage and common question patterns.
 *
 * Seed config for Physics Class 11. Add more subjects/classes as needed.
 * This data informs AI prompt construction so generated content matches
 * actual board exam patterns.
 */

export interface ChapterContext {
  /** Chapter title as it appears in NCERT textbook */
  chapter: string;
  /** Approximate marks weightage in the board exam */
  weightage: number;
  /** Common question patterns observed in previous years */
  questionPatterns: string[];
  /** High-yield topics within this chapter */
  highYieldTopics: string[];
  /**
   * Complete ordered list of NCERT sub-topics for this chapter.
   * When present, the AI MUST cover every topic — no skipping.
   * Order matches the NCERT textbook section order.
   */
  ncertTopics?: string[];
  /** Typical mark distribution */
  markDistribution: {
    oneMark: number;
    twoMark: number;
    threeMark: number;
    fiveMark: number;
  };
  /**
   * Previous Year Question bank — most frequently asked questions from
   * CBSE board exams (2015-2025). Multiple questions per mark category
   * enable regeneration with fresh questions each time.
   */
  pyqBank?: {
    oneMark: string[];
    twoMark: string[];
    threeMark: string[];
    fiveMark: string[];
  };
}

export interface SubjectConfig {
  subject: string;
  classLevel: string;
  board: string;
  totalMarks: number;
  chapters: ChapterContext[];
}

